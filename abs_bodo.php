<h2>Sven-Bodo Scholz's Abstract for talk at MACS PhD Christmas Conference 2017</h2>
<h4>Title: “What is an algorithm, please? or Why (parallel) programming is so hard!”</h4> 
<p>The notions of algorithm, implementation, and runtime  are all intuitively well known.
There even exist a plethora of definitions for these. Yet, in practice, their meaning is highly
context sensitive. This talk sheds some light on this linguistic ambiguity from the perspective 
of trying to map algorithms on parallel hardware. </p>

