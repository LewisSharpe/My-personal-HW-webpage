<h2>Anke Wiese's Abstract for talk at MACS PhD Christmas Conference 2018</h2>
<h4>Title: Monte Carlo Methods in Financial Applications - the Cox-Ingersoll-Ross Process</h4>
<p>In financial applications, economic variables, for example stock prices, interest rates, 
inflation rates, are typically modelled through stochastic equations describing the future
evolution of these variables. Many of these models have no known analytic solution, and
the efficient simulation of these models, while retaining key properties in their 
approximation, is an important aspect for applications.</p>
<br>
<p>In this talk I will discuss some of these methods in the context of the Cox-Ingersoll-Ross
process - a process with many interesting properties  and known to be challenging  
from a computational perspective.</p>

