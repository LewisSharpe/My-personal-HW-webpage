<h4>Title: Joint survival analysis of multiple pension schemes</h4>
<p>We discuss the use of mixture proportional hazards models in survival analysis in order to jointly model individuals' future lifetime on the base of available socio-economic factors. We show also the relevance of the joint survival analysis on relevant financial quantities for pension schemes and pensioners.</p>

