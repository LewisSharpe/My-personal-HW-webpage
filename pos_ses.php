<h3>About: </h3>
<p>This annual poster session is an opportunity for people from the three departments (AMS, CS, and Maths) to get together, and allow PhD students from all three departments to present a poster based on some of their work, and many use this session to practice for other bigger poster events they may have in the future. Usually, posters are based upon the person’s PhD research, although it is doesn’t have to, it can be an area of interest to you outside your PhD. Alternatively, you can display past conference posters, or, Honours'/Masters' projects posters you have done. <b>Posters are usually sized in A1 paper size, and you can present more than one poster if you so desire</b>.</p>
<br>
<p><b>If you want to present a poster at the poster session</b>, please contact, me, Lewis, as your school-wide MACS PGR rep (ls68@hw.ac.uk) <b> by Tuesday, 13th March 2018</b>. </p>
<br>
<p>The school will cover the cost of your poster printing, <b>only if you print through the University's Media Services</b>. If you want to print a poster through the University's Media Services, contact me at ls68@hw.ac.uk, and, I can do a group order for all posters and fill any neccessary paperwork required. Please send your poster to me in <b>PDF format</b> to send to the Media Services for printing by <b>4PM ON TUESDAY, 3RD OF APRIL 2018</b>. As, I will need time to do the paperwork for MACS Finance and provide them copies of all posters after I have done this I will send posters to print services on the afternoon of Friday, 6th April 2018 to allow the turnover for printing.  I will collect all the posters from the bulk order from Media Services, and you can either collect them from me before the event or just collect on the day of the event where I will bring all the posters to the Crush Area. </p>
<br>
<p>If you want to print your own poster through media services without me, you will have to email mediaservices@hw.ac.uk for a quote. You will have to fill a <a href = "form.xls">Media Services Purchase Requistion Form</a> to the MACS School Finance Office. The guildlines for filling in this form and the full process is explained in this document <a href="guildlines.docx"> here</a>.</p>
<br>
<p>Poster boards will be set up for presenters on the day on one side of the Earl Mountbatten Crush Area, and adhesive material to put the posters on the poster boards will be provided, also. Your positions will be marked on the poster boards with poster labels showing your poster number, your name and what department you are presenting from.</p>
<br>
<p>If you no longer want to present a poster, let me know <b>as soon as possible.</b></p>
<br>
<p>Food and drink will be provided during the course of the event, kindly funded by the MACS school. </p>
