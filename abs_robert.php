<h2>Robert Weston's Abstract for talk at MACS PhD Christmas Conference 2017</h2>
<h4>Title: Balls, Boxes and Surfing to Glasgow</h4> 
<p>I shall remind people of the story of the discovery of solitons on the Union Canal, and then go on to describe a toy model for these non-linear waves. The model is a cellular automaton with rules for moving balls in boxes. This  deceptively simple system captures much of the complexity of solitons and has some very interesting modern mathematics behind it - from tropical geometry to quantum groups. Exercises will be set;  bring a pen.</p>

